def test_mutant_lambda_handler(apigw_mutant_dna_event):

    ret = mutant.lambda_handler(apigw_mutant_dna_event, "")
    data = json.loads(ret["body"])

    assert ret["statusCode"] == 200
    assert "message" in ret["body"]
    assert data["message"] == "Mutant!"

def test_human_lambda_handler(apigw_human_dna_event):

    ret = mutant.lambda_handler(apigw_human_dna_event, "")
    data = json.loads(ret["body"])

    assert ret["statusCode"] == 403
    assert "message" in ret["body"]
    assert data["message"] == "Forbidden"

def test_stats_lambda_handler():

    ret = stats.lambda_handler("", "")
    data = json.loads(ret["body"])

    assert ret["statusCode"] == 200
    assert "count_mutant_dna" in ret["body"]
    assert "count_human_dna" in ret["body"]
    assert "ratio" in ret["body"]
